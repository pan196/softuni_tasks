using System;

class FloatOrDouble
{
    static void Main()
    {
        double a = 34.567839023;
        float b = 12.345f;
        double c = 8923.1234857;
        float d = 3456.091f;

        Console.WriteLine("Float numbers are {0} and {1} ", b, d);
        Console.WriteLine("Double numbers are {0} and {1} ", c, a);
    }
}

